export type HSCode = typeof HS_CODES[number];

export const searchHSCodes = (query: string): HSCode[] => {
  const q = query.toLowerCase();
  return HS_CODES.filter(h =>
    h.code.includes(q) ||
    h.description.toLowerCase().includes(q) ||
    h.keywords.toLowerCase().includes(q) ||
    h.category.toLowerCase().includes(q)
  ).slice(0, 10);
};

export const getHSCodeByCode = (code: string): HSCode | undefined => {
  return HS_CODES.find(h => h.code === code);
};

export const getCategories = (): string[] => {
  return [...new Set(HS_CODES.map(h => h.category))];
};
